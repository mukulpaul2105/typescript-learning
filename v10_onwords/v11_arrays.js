"use strict";
// Various approaches to create arrays
// 1. Using Square brackets.
const numbers = [1, 2, 3, 4, 5];
console.log(numbers);
// Using Array Constructor
const number1 = new Array(1, 2, 3, 4, 5, 6);
console.log(number1);
// Using Array.of method
const names = Array.of("Mukul", "Harshit", "Mohit");
console.log(names);
